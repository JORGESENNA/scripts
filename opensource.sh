Lançado em 2015, [ 2 ] é um fork do pfSense , que por sua vez foi bifurcado do m0n0wall construído no FreeBSD . [ 3 ] Quando o m0n0wall fechou em fevereiro de 2015, seu criador, Manuel Kasper, encaminhou sua comunidade de desenvolvedores para o OPNsense. [ 4 ]
