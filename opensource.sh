#QEMU
/*
QEMU é um software livre escrito por Fabrice Bellard que implementa um emulador de processador, permitindo uma virtualização completa de um sistema PC dentro de outro. O QEMU é um hipervisor e é semelhante a projetos como o Bochs, VMware Workstation e o PearPC, mas possui várias características que faltam nestes, incluindo aumento de velocidade em x86, (através de um acelerador) e suporte para múltiplas arquiteturas. Usando tradução dinâmica, atinge uma velocidade razoável, não deixando de ser fácil de converter para novos processadores.
*/

#OpenStack
/*
OpenStack é uma plataforma de computação em nuvem de padrão aberto e gratuita . É implantada principalmente como infraestrutura como serviço (IaaS) em nuvens públicas e privadas, onde servidores virtuais e outros recursos são disponibilizados aos usuários. [ 2 ] A plataforma de software consiste em componentes inter-relacionados que controlam diversos pools de hardware de vários fornecedores de recursos de processamento, armazenamento e rede em um data center . Os usuários o gerenciam por meio de um painel baseado na web, por meio de ferramentas de linha de comando ou por meio de serviços da web RESTful .

O OpenStack começou em 2010 como um projeto conjunto da Rackspace Hosting e da NASA . Em 2012 , era administrado pela OpenStack Foundation , uma entidade corporativa sem fins lucrativos criada em setembro de 2012 [ 3 ] para promover o software OpenStack e sua comunidade. [ 4 ] Em 2018, mais de 500 empresas aderiram ao projeto. [ 5 ] Em 2020, a fundação anunciou que seria renomeada para Open Infrastructure Foundation em 2021. [ 6 ]
*/

#OPNSense
